
public class switchEx 
{
	public static void main(String args[])
	{
		char c = 'x';
		switch(c)
		{
			case 'a' : 	System.out.println("The character is a ");
						break;
						
			case 'b' : 	System.out.println("The character is b ");
						break;
			
			default : 	System.out.println("Unknown value");
		}
	}
}
